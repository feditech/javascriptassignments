// 2. Write a program that alerts the current month in words.
// For example December.

var date = new Date();

alert("Current month")
switch (date.getMonth()) {
    case 0:
        alert("january")
        break
    case 1:
        alert("Febuary")
        break
    case 2:
        alert("March")
        break
    case 3:
        alert("April")
        break
    case 4:
        alert("May")
        break
    case 5:
        alert("June")
        break
    case 6:
        alert("July")
        break
    case 7:
        alert("August")
        break
    case 8:
        alert("September")
        break
    case 9:
        alert("October")
        break
    case 10:
        alert("November")
        break
    case 11:
        alert("December")
        break
}
