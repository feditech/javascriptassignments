document.write("<br>"+"Counting"+ "<br>")
for( var i = 1; i<=10 ;i++){
    document.write(i+ "<br>")
}

document.write("<br>"+"Reverse Counting"+ "<br>")
for( var i = 10; i>=1 ;i--){
    document.write(i+ "<br>")
}

document.write("<br>"+"Even"+ "<br>")
var i =2;
while( i<=20){
    document.write(i+ "<br>")
i = i + 2;
}


document.write("<br>"+"Odd"+ "<br>")
var i =1;
while( i<=20){
    document.write(i+ "<br>")
i = i + 2;
}

document.write("<br>"+"Series"+ "<br>")
var i =2;
while( i<=20){
    document.write(i+"k" + "<br>")
i = i + 2;
}
