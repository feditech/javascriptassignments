document.write("<br>"+"Counting"+ "<br>")
for( var i = 1; i<=10 ;i++){
    document.write(i+ "<br>")
}

document.write("<br>"+"Reverse Counting"+ "<br>")
for( var i = 10; i>=1 ;i--){
    document.write(i+ "<br>")
}

document.write("<br>"+"Even"+ "<br>")
for( var i = 2; i<=20 ;i+2){
    document.write(i+ "<br>")
}